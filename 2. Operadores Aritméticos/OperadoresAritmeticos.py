# Operadores Aritméticos
# Suma
print(2 + 2)

# Resta
print(150 - 75)

# Multiplicación
print(10 * 10)

# División
print(75 / 9)

# Exponenciación
print(2 ** 3)

# División Entera (Sólo nos devuelve la parte entera del cociente)
print(75 // 9)

# El módulo de una división es el resto de la división multiplicado por el divisor.
print(75 % 9)