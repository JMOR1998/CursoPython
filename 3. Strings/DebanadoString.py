# Debanado de Strings
cadena = "Este es un ejemplo de SubString (Debanado de Cadenas)"
print(len(cadena)) # La función "len" devuelve el tamaño de la cadena
print(cadena[2]) # Corresponde a la "t" de la palabra "Este", se empieza desde el 0
print(cadena[0:10]) # Devuelve "Este es un"
print(cadena[:20]) # Devuelve "Este es un ejemplo d"
print(cadena[20:]) # Devuelve "e SubString (Debanado de Cadenas)"
print(cadena[-1]) # Devuelve ")", porque empieza a recorrer la cadena desde atras
print(cadena[-3]) # Devuelve "a"