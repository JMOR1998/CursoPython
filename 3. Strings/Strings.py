# Cadenas o Strings
cadena = "Esto es un ejemplo de cadena de texto" # Se puede poner con comillas dobles.
print(cadena)
cadena = 'Esto es un ejemplo de cadena de texto' # Se puede poner también con comillas simples.
print(cadena)
# Si necesitamos escapar algún caracter usamos \
cadena = "Esto es un \"ejemplo\" de cadena\b de texto"
print(cadena)
# Si queremos hacer un salto de línea se hace con \n.
# Si queremos hacer una tabulación  se hace con \t.
# Si queremos quitar la última letra o espacio en blanco usamos \b posterior a lo mencionado anteriormente.
# Si queremos hacer un salto de línea pero mantener la distancia usamos \v.